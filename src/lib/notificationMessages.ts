/** デフォルトキャラ（未設定ユーザー向け） */
export const DEFAULT_CHARACTER = 'aoi'

/**
 * キャラと時間帯からランダムにメッセージを1つ返す
 */
export function pickMessage(characterId: string, slot: NotificationSlot): string {
  const messages =
    NOTIFICATION_MESSAGES[characterId]?.[slot] ??
    NOTIFICATION_MESSAGES[DEFAULT_CHARACTER][slot]
  return messages[Math.floor(Math.random() * messages.length)]
}

